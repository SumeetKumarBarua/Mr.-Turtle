package com.sumeet;

public class Arena {

	private Integer xPos;
	private Integer yPos;
	
	public Arena(Integer xPos, Integer yPos) {
		super();
		this.xPos = xPos;
		this.yPos = yPos;
	}

	public Integer getxPos() {
		return xPos;
	}

	public void setxPos(Integer xPos) {
		this.xPos = xPos;
	}

	public Integer getyPos() {
		return yPos;
	}

	public void setyPos(Integer yPos) {
		this.yPos = yPos;
	}
	
	
	
}
